//=====================================================================================
//Функция поиска подстрок в строке
//Входные данные:
//                      user:String          - список категорий у пользователя
//                      kategkod:String      - категория
//Возращаемое значение:
//                      ret:Boolean          - да/нет
//=====================================================================================
fun findStrStr(user:String, kategkod:String):Boolean{
    var strTek=""
    var ret=false
    for(x in user){
        if(x=='_'){
            strTek=""
        }
        else{
            strTek+=x
        }
        if(strTek==kategkod) ret=true
    }
    return ret
}