//Функция поиска пользователя по id
//Входные данные:
//                      id:Int          - адресс
//Возращаемое значение:
//                      ret:User        - пользователь
//=====================================================================================
fun findUser(id:Int):User{
    var userRet:User=User(-1, "Error", "Error", "-1",-1)
    for(user in userList){
        if(id == user.id)  userRet=user
    }
    return userRet
}

